    public FecharComanda(Comanda comanda) {
        initComponents();
        this.comanda = comanda;
        this.comanda.calculaValorParcial();
        this.lblValorDescricao.setText("R$ " + String.format("%.2f", this.comanda.getValorTotal()));

        double desconto;
        try {
            desconto = this.comanda.aplicarDesconto();
        } catch (Exception e) {
            desconto = 0;
        }
        this.lblDescontoDescricao.setText("- R$ " + String.format("%.2f", desconto));
        
        this.comanda.calculaValorParcial();
        if (this.comanda.getMesa() != null) { 
            // para quando o pedido for no salão
            this.comanda.setValorTotal(comanda.getValorTotal()*1.1);
            this.lblAdicional.setText("Valor com 10%:");
            this.lblAdicionalDescricao.setText("R$ " + String.format("%.2f", this.comanda.getValorTotal()));
            
        } else { // para quando o pedido for delivery
            this.comanda.setValorTotal(this.comanda.getValorTotal()+13.90);
            this.lblAdicional.setText("Valor com frete:");
            this.lblAdicionalDescricao.setText("R$ " + String.format("%.2f", this.comanda.getValorTotal()));
        }
        this.comanda.setValorTotal(this.comanda.getValorTotal()-desconto);
        this.lblValorFinalDescricao.setText("R$ " + String.format("%.2f", this.comanda.getValorTotal()));
        
        if (this.comanda.getCliente() != null)
            this.lblClienteDescricao.setText(this.comanda.getCliente().getCPF());
    }

