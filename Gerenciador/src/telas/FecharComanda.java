    private void btnFinalizarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarPedidoActionPerformed
        // faz todas as operações para fechar a comanda
        this.setVisible(false);
        JOptionPane.showMessageDialog(null, "Pedido finalizado com sucesso.", "Finalizado", JOptionPane.INFORMATION_MESSAGE);
        double valorComanda = this.comanda.getValorTotal();
        this.comanda.fecharComanda();
        
        // ALTERANDO O ARQUIVO DE INFORMAÇÕES GERAIS
        String dadosGerais;
        Object[] dadosArquivo = ManipulaArquivos.importandoDadosGerais();
        ManipulaArquivos.apagarTxt("Gerais.txt");
        double receita = Double.parseDouble(dadosArquivo[0].toString()) + valorComanda; // calcula a receita total com a nova comanda e escreve no arquivo
        int quantPedidos = Integer.parseInt(dadosArquivo[1].toString())+1;
        dadosGerais = (receita+", "+ quantPedidos);
        ManipulaArquivos.escreverTxt("Gerais.txt", dadosGerais);
        
        // ALTERANDO OS ARQUIVOS DE CLIENTES E FUNCIONÁRIOS
        ManipulaArquivos.apagarTxt("Clientes.txt");
        for (Cliente c : Controle.clientes) {
            String dadosCliente;
            dadosCliente = (c.getNome()+", "+ c.getCPF()+", "+ c.getEmail()+", "+c.getTelefone()+", "+c.getEndereço()+", "+ c.getAniversario().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"))+ ", "+ c.getQuantidadePedidos()+"\n");
            ManipulaArquivos.escreverTxt("Clientes.txt", dadosCliente);
        }
        
        ManipulaArquivos.apagarTxt("Funcionarios.txt");
        for (Funcionario f : Controle.funcionarios) {
            String dadosFuncionario;
            if (f instanceof Gerente)
                dadosFuncionario = ("Gerente, " + f.getNome()+", "+ f.getCPF()+", "+ f.getSalario()+", "+ f.getDiaContratado().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"))+ ", "+((Gerente)f).getSenha()+"\n");
            else if (f instanceof Garcom)
                dadosFuncionario = ("Garcom, " + f.getNome()+", "+ f.getCPF()+", "+ f.getSalario()+", "+f.getDiaContratado().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"))+", " + ((Garcom)f).getQuantidadeAtendimentos()+ "\n");
            else
                dadosFuncionario = ("Entregador, " + f.getNome()+", "+ f.getCPF()+", "+ f.getSalario()+", "+f.getDiaContratado().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"))+", "+((Entregador)f).getCNH()+", " + ((Entregador)f).getQuantidadeEntregas()+ "\n");
            ManipulaArquivos.escreverTxt("Funcionarios.txt", dadosFuncionario);
        }
    }//GEN-LAST:event_btnFinalizarPedidoActionPerformed

